uint8 Button_GetValue(uint8 But_Num)
{
	uint8 Button_Value = 0 , Temp_Value = 0;
	
	Button_Value = GET_BIT(DIO_PORTD->PIN, But_Num);
	
	while(Temp_Value == 0)
	{
		Temp_Value = GET_BIT(DIO_PORTD->PIN, But_Num);
	}
	
	_delay_ms(10);
	
	return Button_Value;
}

